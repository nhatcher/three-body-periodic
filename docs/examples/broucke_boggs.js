function buildOrbit({ m1, m2, m3, x1, v1, x2, v2, t}) {
    const m = (m1 + m2 + m3);
    const xcm = (m1 * x1 + m2 * x2 + m3 * 0) / m;
    const v3 = (m1 * v1 + m2 * v2) / m;

    const x1c = x1 - xcm;
    const x2c = x2 - xcm;
    const x3c = -xcm;

    return [
        [
            x1c, 0, 0, 0, v1 - v3, 0, m1,
            x2c, 0, 0, 0, v2 - v3, 0, m2,
            x3c, 0, 0, 0, -v3, 0, m3
        ],
        t,
        0.0,
    ];

}

