function getOrbit(i) {
    const data = ic[i];
    const x = data[1];
    const y = data[2];
    const t = data[3];
    const m = data[0].match(/^[A-Za-z]+\d*\(\s*([^)]*?)\s*\)$/);
    const [m1, m3, m2] = m[1].split(/\s*,\s*/).map(parseFloat);
    return [
        [-0.5, 0, 0, 0, 0, 0, m1,
            x, y, 0, 0, 0, 0, m2,
            0.5, 0, 0, 0, 0, 0, m3],
        t
    ];
}

function getNames() {
    return ic.map((item) => item[0]);
}

